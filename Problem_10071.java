import java.util.*;
class Problem_10071
{
    static Scanner sc=new Scanner(System.in);
    public static void main(String args[])
    {
        int v=0;
        int t=0;
        while(sc.hasNext())
        {
            v=sc.nextInt();
            t=sc.nextInt();
            System.out.println(2*v*t);
        }
    }
}
